namespace Metroit.Spreadsheet.Utilities.Core.Mapping
{
    /// <summary>
    /// 罫線のスタイルを提供します。
    /// </summary>
    public enum LineStyle
    {
        /// <summary>
        /// 罫線なし を示します。
        /// </summary>
        LineStyleNone,

        /// <summary>
        /// 実線 を示します。
        /// </summary>
        Continuous,

        /// <summary>
        /// 破線 を示します。
        /// </summary>
        Dash,

        /// <summary>
        /// 一点鎖線 を示します。
        /// </summary>
        DashDot,

        /// <summary>
        /// 二点鎖線 を示します。
        /// </summary>
        DashDotDot,

        /// <summary>
        /// 点線 を示します。
        /// </summary>
        Dot,

        /// <summary>
        /// 二重線 を示します。
        /// </summary>
        Double,

        /// <summary>
        /// 斜め斜線 を示します。
        /// </summary>
        SlantDashDot
    }
}
